    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String user = jTextField1.getText().trim();

        if(user.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Enter username to delete");
            return;
        }

        if(!usuarios.contains(user)){
            javax.swing.JOptionPane.showMessageDialog(this, "User not found");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete this user?",
            "Confirm delete",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if(confirm == javax.swing.JOptionPane.YES_OPTION){

            int index = usuarios.indexOf(user);

            // 🔥 eliminar de TODAS las listas
            usuarios.remove(index);
            passwords.remove(index);
            estados.remove(index);
            roles.remove(index);
            intentos.remove(index);

            // guardar cambios en archivo
            Usermaintenance.guardarUsuarios();

            javax.swing.JOptionPane.showMessageDialog(this, "User deleted successfully");

            // limpiar campos
            jTextField1.setText("");
            jPasswordField1.setText("");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);

            jButton1.setEnabled(true);
            jButton2.setEnabled(false);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

