    public static void guardarUsuarios() {
    try (java.io.PrintWriter pw = new java.io.PrintWriter("usuarios.txt")) {
        for (int i = 0; i < usuarios.size(); i++) {
            pw.println(
                usuarios.get(i) + "," +
                passwords.get(i) + "," +
                estados.get(i) + "," +
                roles.get(i)
            );
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }
    
    public static void cargarUsuarios() {
        
    usuarios.clear();
    passwords.clear();
    estados.clear();
    roles.clear();
    intentos.clear();

    try (java.io.BufferedReader br = new java.io.BufferedReader(new java.io.FileReader("usuarios.txt"))) {
        String linea;

        while ((linea = br.readLine()) != null) {
            String[] datos = linea.split(",");

            usuarios.add(datos[0]);
            passwords.add(datos[1]);
            estados.add(datos[2]);
            roles.add(datos[3]);

            //sincronizar intentos
            intentos.add(0);
        }

    } catch (Exception e) {
        System.out.println("Archivo no encontrado, se creará uno nuevo.");
    }
}
    
