    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String user = jTextField1.getText().trim();

        if(user.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Enter username to search");
            return;
        }

        if(!usuarios.contains(user)){
            javax.swing.JOptionPane.showMessageDialog(this, "User not found");
            jButton1.setEnabled(true);  // permitir crear
            jButton2.setEnabled(false); // bloquear update
            jButton5.setEnabled(false);
            return;
        }

        int index = usuarios.indexOf(user);

        jButton2.setEnabled(true);  // activar Update
        jButton1.setEnabled(false); // desactivar Add
        jButton5.setEnabled(true);

        //    carga datos en los campos
        jPasswordField1.setText(passwords.get(index));
        jComboBox2.setSelectedItem(estados.get(index)); // Status
        jComboBox1.setSelectedItem(roles.get(index));   // Role

        javax.swing.JOptionPane.showMessageDialog(this, "User loaded");
    }//GEN-LAST:event_jButton3ActionPerformed

