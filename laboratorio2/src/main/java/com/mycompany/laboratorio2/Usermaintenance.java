    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String user = jTextField1.getText().trim();
        String pass = new String(jPasswordField1.getPassword()).trim();
        String role = jComboBox1.getSelectedItem().toString();
        String status = jComboBox2.getSelectedItem().toString();

        if(user.isEmpty() || pass.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Complete all fields");
            return;
        }

        if(!usuarios.contains(user)){
            javax.swing.JOptionPane.showMessageDialog(this, "User not found");
            return;
        }

        int index = usuarios.indexOf(user);

        if(index == -1){
            javax.swing.JOptionPane.showMessageDialog(this, "User not found");
            return;
        }

        passwords.set(index, pass);
        estados.set(index, status);
        roles.set(index, role);

        Usermaintenance.guardarUsuarios();

        javax.swing.JOptionPane.showMessageDialog(this, "User updated successfully");

        jTextField1.setText("");
        jPasswordField1.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String user = jTextField1.getText();
        String pass = new String(jPasswordField1.getPassword());
        String role = jComboBox1.getSelectedItem().toString();
        String status = jComboBox2.getSelectedItem().toString();

        // Validar campos vacíos
        if(user.trim().isEmpty() || pass.trim().isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Complete all fields");
            return;
        }

        // Verificar si ya existe
        if(usuarios.contains(user)){
            javax.swing.JOptionPane.showMessageDialog(this, "User already exists");
            return;
        }

        // Guardar usuario
        intentos.add(0);
        usuarios.add(user);
        passwords.add(pass);
        estados.add(status);
        roles.add(role);

        javax.swing.JOptionPane.showMessageDialog(this, "User added successfully");

        Usermaintenance.guardarUsuarios();

        // Limpiar campos
        jTextField1.setText("");
        jPasswordField1.setText("");
        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);

    }//GEN-LAST:event_jButton1ActionPerformed

